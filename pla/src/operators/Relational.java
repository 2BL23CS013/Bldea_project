package operators;

public class Relational {
    public static void main(String[] args) {
        System.out.println(10 > 5);   // true
        System.out.println(10 < 5);   // false
        System.out.println(10 >= 5);  // true
        System.out.println(10 <= 5);  // false
        System.out.println(10 == 5);  // false
        System.out.println(10 != 5);  // true
    }
}
